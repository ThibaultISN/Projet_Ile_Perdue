public abstract class Carte {
    private String type;

    
    Carte(){
        
    }
    
    public void afficher(){
        System.out.println(this.getType() +" ");
         
}

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }
}
